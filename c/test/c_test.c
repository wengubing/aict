#include <stdio.h>

int main() {
    // 初始化变量：数值看似简单，实则隐藏溢出陷阱
    char a = 127;  // 有符号char的最大值（范围-128~127）
    int b = 5, c = 0;

    // 核心迷惑表达式：优先级、短路、自增/自减、类型溢出叠加
    c = (a++) + (++b) && (a > b--) || (--c);

    // 打印最终结果：第一眼极难猜对a/b/c的真实值
    printf("a = %d, b = %d, c = %d\n", a, b, c);

    //预期输出结果a = -128, b = 5, c = 1

    return 0;
}